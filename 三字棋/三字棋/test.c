void game()
{
	char ret = 0;
	char board[ROW][COL] = { 0 };
	//初始化棋盘
	initboard(board, ROW, COL);
	//打印棋盘
	dispalyBoard(board, ROW, COL);
	//下棋
	while (1)
	{
		//玩家下棋
		PalyerMove(board, ROW, COL);
		dispalyBoard(board, ROW, COL);
		//判断玩家是否赢
		ret = IsWin(board, ROW, COL);
		if (ret != 'C')
		{
			break;
		}
		if (ret == '#')
		{
			printf("玩家赢\n");
		}
		else if (ret == '*')
		{
			printf("电脑赢\n");
		}
		else
		{
			printf("平局\n");
		}

			//电脑下棋
		ComputerMove(board, ROW, COL);
		dispalyBoard(board, ROW, COL);
	}
	
}
void test()
{
	int input = 0;
	srand((unsigned int)time(NULL));
	do
	{
		menu();
			printf("请选择>:");
		scanf("%d", &input);
		switch (input)
		{
		case 1:
			game();
			break;
		case 0:
			printf("退出游戏\n");
			break;
		default:
			printf("选择错误，请重新选择\n");
			break;
		}
	} while (input);
}
int main()
{
	test();
	return 0;
}